// === GALLERY LIGHTBOX ===
function initGallery() {
  const items = document.querySelectorAll('.gallery-item');
  if (!items.length) return;

  const lightbox = document.getElementById('lightbox');
  const lbImg    = document.getElementById('lightbox-img');
  const lbCaption = document.getElementById('lightbox-caption');
  if (!lightbox || !lbImg) return;

  let currentIndex = 0;
  const images = Array.from(items).map(item => ({
    src:     item.querySelector('img').src,
    alt:     item.querySelector('img').alt,
    caption: item.dataset.caption || ''
  }));

  function showImage(index) {
    currentIndex = (index + images.length) % images.length;
    lbImg.src = images[currentIndex].src;
    lbImg.alt = images[currentIndex].alt;
    if (lbCaption) lbCaption.textContent = images[currentIndex].caption;
  }

  function openLightbox(index) {
    showImage(index);
    lightbox.classList.add('open');
    document.body.style.overflow = 'hidden';
  }

  function closeLightbox() {
    lightbox.classList.remove('open');
    document.body.style.overflow = '';
  }

  items.forEach((item, i) => {
    item.addEventListener('click', () => openLightbox(i));
    item.addEventListener('keydown', e => {
      if (e.key === 'Enter' || e.key === ' ') openLightbox(i);
    });
    item.setAttribute('role', 'button');
    item.setAttribute('tabindex', '0');
  });

  document.getElementById('lightbox-close')?.addEventListener('click', closeLightbox);
  document.getElementById('lightbox-prev')?.addEventListener('click', () => showImage(currentIndex - 1));
  document.getElementById('lightbox-next')?.addEventListener('click', () => showImage(currentIndex + 1));

  lightbox.addEventListener('click', e => {
    if (e.target === lightbox) closeLightbox();
  });

  document.addEventListener('keydown', e => {
    if (!lightbox.classList.contains('open')) return;
    if (e.key === 'Escape') closeLightbox();
    if (e.key === 'ArrowLeft') showImage(currentIndex - 1);
    if (e.key === 'ArrowRight') showImage(currentIndex + 1);
  });
}

// === PREMIUM — DAILY BAKE COUNTER ===
// Animates count-up when section scrolls into view; numbers drift slightly
// over the day so the "live" feel holds without a backend.
function initBakeCounter() {
  const stats = document.querySelectorAll('.counter-stat-number[data-counter-target]');
  if (!stats.length) return;

  const now = new Date();
  const minutesSinceOpen = Math.max(0, (now.getHours() - 6) * 60 + now.getMinutes());
  const dayProgress = Math.min(1, minutesSinceOpen / (11 * 60));

  function animateTo(el, target) {
    const duration = 1400;
    const start = performance.now();
    const from = 0;
    function tick(t) {
      const p = Math.min(1, (t - start) / duration);
      const eased = 1 - Math.pow(1 - p, 3);
      el.textContent = Math.round(from + (target - from) * eased);
      if (p < 1) requestAnimationFrame(tick);
      else el.textContent = target;
    }
    requestAnimationFrame(tick);
  }

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (!entry.isIntersecting) return;
      stats.forEach(el => {
        const baseTarget = parseInt(el.getAttribute('data-counter-target'), 10) || 0;
        const liveTarget = Math.round(baseTarget * (0.4 + 0.6 * dayProgress));
        animateTo(el, liveTarget);
      });
      observer.disconnect();
    });
  }, { threshold: 0.4 });

  observer.observe(stats[0].closest('.bake-counter'));

  const timeEl = document.getElementById('counter-time');
  if (timeEl) {
    const hh = String(now.getHours()).padStart(2, '0');
    const mm = String(now.getMinutes()).padStart(2, '0');
    timeEl.textContent = `${hh}:${mm}`;
  }
}

// === PREMIUM — SOURDOUGH TIMELINE ===
// Highlights the step that matches the current hour-of-day so the timeline
// reads as "right now we're at: ferment". Scrolling triggers a stagger reveal.
function initTimeline() {
  const track = document.getElementById('timeline-track');
  if (!track) return;

  const steps = track.querySelectorAll('.timeline-step');
  const hour = new Date().getHours();

  let activeIndex;
  if (hour >= 17 && hour < 20)      activeIndex = 0; // milling
  else if (hour >= 20 && hour < 23) activeIndex = 1; // starter fed
  else if (hour >= 23 || hour < 6)  activeIndex = 2; // mixing → long ferment overlap
  else if (hour >= 6  && hour < 7)  activeIndex = 4; // in oven
  else                              activeIndex = 3; // ferment (default daytime)

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (!entry.isIntersecting) return;
      steps.forEach((step, i) => {
        setTimeout(() => {
          if (i <= activeIndex) step.classList.add('active');
        }, i * 220);
      });
      const progress = (activeIndex / (steps.length - 1)) * 100;
      track.style.setProperty('--progress', progress + '%');
      observer.disconnect();
    });
  }, { threshold: 0.3 });

  observer.observe(track);

  const nowEl = document.getElementById('tl-now-step');
  if (nowEl) nowEl.textContent = t(`tl${activeIndex + 1}.title`);
}

